# 매개변수를 지정하여 호출하기 
def sub (a, b):
    return a - b
result = sub(a=7, b=3) # a에 7, b에 3을 전달
myprint(result)
# 매개변수를 지정하면 순서에 상관없이 사용할 수있다는 장점이 있음
result = sub(b=5, a=3)
myprint(result)


# 입력값이 몇 개가 될지 모를 때 
# def 함수_이름(*매개변수):
#     수행할_문장
#     ...
# 여러 개의 입력값을 받는 함수 만들기 
def add_many(*args):
    result = 0
    for i in args:
        result += i
    return result
# 여려개의 입력값을 모두 더하는 함수
