# 함수를 사용하는 이유
# 1) 반복된 작업을 수행할 때 
# 2) 자신이 작성한 프로그램을 기능 단위의 함수로 분리해 놓으면 프로그램 흐름을 일목요연하게 볼 수 있음



# 파이썬 함수의 구조 
# def 함수_이름(매개변수):
#     수행할_문장1
#     수행할_문장2
#     ...
# def 함수는 함수를 만들 떄 사용하는 예약어이며, 함수 이름은 함수를 만드는 사람이 임의로 만들 수 있음 
# (예약어 : 파이썬 언어가 자체적인 문법 규칙을 위해 미리 쓰임새를 '에약'해 둔 단어, 에약어는 변수 이름이나 함수이름으로 절대 사용 불가)
# 함수 이름 뒤 괄호 안의 매개변수는 이 함수에 입력으로 전달되는 값을 받는 변수
# 이렇게 함수를 정의한 후 if, while, for문 등과 마찬가지로 함수에서 수행할 문장 입력
def add(a, b):
    return a + b
