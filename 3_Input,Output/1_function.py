# 여기서 return은 함수의 결과값(리턴값)을 리턴하는 명령어임 
a = 3
b = 4
c = add(a, b)
myprint(c)


# 매개변수와 인수 
# 매개변수(parameter) : 함수에 입력으로 전달된 값을 받는 변수
# 인수(arguments) : 함수를 호출할 때 전달하는 입력값을 의미 
# def add(a, b): <- a,b는 매개변수
#    return a + b
# print(add(3, 4)) <-3,4는 인수



# 입력값과 리턴값에 따른 함수의 형태 
# 1) 일반적인 함수 
# 입력값과 리턴값이 있는 함수 
# def 함수_이름(매개변수):
#     수행할_문장
#     ...
#     return 리턴값 
def add(a, b):
    result = a + b
    return result
a = add(3, 4)
myprint(a) 
