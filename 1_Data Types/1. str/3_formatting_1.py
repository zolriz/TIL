'''문자열 포메팅 연습'''

# 숫자 포메팅
print("I eat %d apples.\n\n" % 3)

# 문자 포메팅
print ("I eat %s apples.\n\n" %"five")

# 포메팅 두개 쓰기
number = 10
day = "three"
print("I ate %d apples. so I was sick for %s days.\n\n" %(number, day)) 

# %s 뒤엔 숫다, 소수, 문자 다 가능하다 (숫자를 문자열로 만드는 것임)
print("I have %s apples" % 3, "rate is %s\n\n" % 3.234)

# %표시하기
print("Error is %d%%" % 98)



